                                <h3>DESCRIPCIÓN DE ASIGNATURAS DIPLOMADO GESTIÓN DE TECNOLOGÍAS DE INFORMACIÓN</h3>
                                <br>

                                <h1>Estrategia de Empresas para la Gestión de Tecnologías</h1>
                                <br>
                                <p> El propósito de este curso es que el estudiante conozca qué es una estrategia de negocios y los principales marcos de referencia para entender las distintas opciones estratégicas que puede tener una empresa principalmente orientado a la gestión de TI. Las clases de desarrollarán básicamente a través del estudio de casos.</p>
                                
                                <h1>Gestión de Sistemas y TI</h1> 
                                <br >
                                <p> El curso provee una introducción a los conceptos de sistemas y tecnologías de información y su aplicación en el negocio, identificando los conceptos claves y cómo las tecnologías de información apoyan a la gestión de la empresa en el mejoramiento del rendimiento de la organización.</p>
                                
                                <h1>Modelamiento de Procesos</h1>
                                <br>    
                                <p>El curso tiene como objetivo dotar al alumno del dominio de un conjunto de técnicas y del uso de herramientas software que faciliten el modelamiento y análisis de los procesos de negocios. Se verán entre otras técnicas: BPM, UML, DFD, etc. Las clases serán principalmente prácticas.</p>
                                <br>
                                
                                <h3>DIPLOMADO GESTIÓN DE INNOVACIÓN Y EMPRENDIMIENTO</h3>  
                                <br>

                                <h1>Creatividad e Innovación</h1>
                                <br>
                                <p>El objetivo general del curso es que los participantes adquieran los conocimientos para buscar problemas relevantes que requieran soluciones creativas e identificar oportunidades de negocios, siendo capaces de identificar conceptos como observación, brainstorming, prototipos e implementación en el mercado.</p> 
                                    
                                
                                <h1>Gestión de Innovación</h1>
                                <br >
                                <p>El objetivo general del curso es que los participantes adquieran los conocimientos para gestionar los procesos de innovación en la empresa alineándola con su estrategia. Para ello se analizarán los fundamentos de un sistema de innovación y los mecanismos y herramientas para su gestión. Se revisarán además las tendencias modernas en innovación con especial énfasis en los sistemas abiertos de innovación.</p> 
                                
                                <h1>Emprendimiento</h1>  
                                <br >
                                <p>En este curso los estudiantes aprenderán los atributos esenciales de un emprendedor y los estados por los cuales pasa desde que se crea la idea de emprendimiento hasta generar un negocio exitoso. Este curso se dirige a proveer una introducción a la aventura y proceso de creación. El curso se dirige particularmente a quienes quieren transformarse en emprendedor. Es también útil para quienes quieran interactuar con emprendedores durante sus carreras. Finalmente, este curso es útil para cualquiera con una disposición a combinar análisis serio con pensamiento crítico.</p>
                                <br>

                                <h3>DIPLOMADO PROCESOS INTEGRALES DE SOFTWARE</h3>
                                <br>

                                <h1>Ingeniería de Requerimientos</h1>
                                <br>
                                <p>El curso provee al alumno de un conocimiento cabal y práctico acerca de un conjunto de técnicas de educción de requerimientos que le ayudarán a capturar información relevante acerca de una problemática o necesidad de la organización que requiera de la evaluación de alternativas de proyectos relacionados con el software o la tecnología.</p> 
                                
                                <h1>Gestión de Calidad del Software</h1>
                                <br>
                                <p>Este curso entrega a sus alumnos una formación en el área de la calidad de procesos y productos software, en la implementación de programas de calidad tanto en el ámbito privado como público y en los modelos de madurez y normas existentes de calidad del software, así como su aplicación en las organizaciones.</p> 

                                <h1>Evaluación de Proyectos Software</h1>
                                <br>
                                <p>Este curso tiene por objetivo que los participantes puedan explicar conceptos y métodos fundamentales de evaluación económica de proyectos y aplicarlos a las decisiones de inversión en informática, principalmente en aquellos que el software tiene un predominio.</p> 

                                <h1>Auditoría Informática</h1>
                                <br>
                                <p>Este curso presenta los conceptos fundamentales, técnicas y métodos, aspectos organizativos, jurídicos sobre control interno y auditoría informática, principalmente en áreas como seguridad, desarrollo, mantenimiento, física, explotación, calidad, redes, etc.</p>

                                <h1>Proyecto de Grado</h1>
                                <br >
                                <p>En este curso los alumnos desarrollarán un proyecto para optar a la obtención del grado de acuerdo al reglamento respectivo y a las condiciones del programa. Este proyecto considerará deberá considerar conceptos y metodologías vistas en las demás asignaturas del programa y utilizarlas en la solución a una problemática organizacional.</p>

