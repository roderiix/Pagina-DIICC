                                <h6>Fondo: Proyectos internos de investigación DIUDA 2017 (Categoría Regular)  </h6>
                                <p>Antecedentes: El uso de dispositivos móviles ha crecido sustancialmente en los últimos tiempos, principalmente para permitir a una gran variedad de tipos de usuarios acceder a diferentes servicios y realizar sus transacciones desde cualquier lugar. Desde esta perspectiva, la usabilidad de las aplicaciones móviles juega un papel fundamental para que el usuario realice de forma adecuada sus actividades. Sin embargo, las aplicaciones móviles presentan aún muchas dificultades en este ámbito. Por tanto, se hace necesario considerar a la usabilidad como un factor primordial en el proceso de desarrollo de aplicaciones móviles.</p>
                                <p> Además, generalmente las técnicas de usabilidad están pensadas para procesos de diseño centrados en el usuario y para aplicaciones de escritorio. Muchas de estas técnicas son anteriores al auge de las aplicaciones para dispositivos móviles, así que no están adaptadas para abordar el enfoque móvil.</p>

                                <p>Objetivo: El objetivo del proyecto de investigación consiste en determinar cuales son los métodos, técnicas y modelos que son usados para evaluar la usabilidad de las aplicaciones móviles multiplataforma (AMM) y si los factores principales que determinan la usabilidad de un producto software como condiciones específicas, necesidades, usuarios y particularidades de los dispositivos móviles se toman en cuenta tanto en el proceso de evaluación de usabilidad de este tipo de aplicaciones como en la creación del diseño del interacción. Además, investigar cómo están siendo aplicadas las técnicas de evaluación de la usabilidad en el desarrollo de las AMM.</p>

                                <p>Metodología: Se utilizará una metodología deductiva-cualitativa con una investigación exploratoria, la cual se realizará a través de una revisión sistemática de literatura para analizar las técnicas, métodos y modelos utilizados para evaluar la usabilidad de las AMM.</p>

                                <p>Resultados Esperados. Como resultados, se espera identificar:</p>

                                <p class="quote">Cuales son los principales problemas y aspectos relevantes relacionados con el proceso de evaluación de usabilidad de las AMM, es decir qué técnicas se están aplicando, cómo se están aplicando y en que momento del ciclo de vida del producto software están siendo usadas.</p>
                                <p class="quote">Si en la literatura existen técnicas automatizadas para la evaluación de la usabilidad, particularmente con un enfoque multiplataforma.
                                Cómo el diseño de la interacción difiere entre las principales plataformas móviles existentes y los efectos que esta característica ocasiona a los usuarios finales al usar una aplicación.</p>
                                <p class="quote">Si factores específicos de usabilidad como usuarios, tareas y contexto de uso son considerados por los desarrolladores en la etapa de creación del diseño de la interacción y por evaluadores para medir la usabilidad.</p>
